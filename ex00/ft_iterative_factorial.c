int	ft_iterative_factorial(int nb)
{
	int	a;

	a = nb;
	if (nb == 0)
		return (1);
	if (nb > 0 && nb < 13)
	{
		while (a != 1)
		{
			nb *= (a - 1);
			a--;
		}
		return (nb);
	}
	else
		return (0);
}
